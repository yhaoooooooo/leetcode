    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {

        if (headA == null) {
            return null;
        }
        if (headB == null) {
            return null;
        }
        ListNode tmpA = headA;
        ListNode tmpB = headB;

        ListNode intersectionNode = null;
        int cycleCount = 0;
        while (true) {
            if (tmpA == tmpB) {
                intersectionNode = tmpA;
                break;
            }
            if (tmpA.next == null) {
               cycleCount++;
                if (cycleCount > 1) {
                    break;
                }
               tmpA = headB;
            } else {
                tmpA = tmpA.next;
            }

            if (tmpB.next == null) {
                tmpB = headA;
            } else {
                tmpB = tmpB.next;
            }
        }

        return intersectionNode;
    }

